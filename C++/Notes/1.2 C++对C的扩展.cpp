/*
--------------------------------------------------
【C++中所有的变量和函数都必须有类型】

C语言：	
	1.函数参数不写类型：可以是任意类型
	2.函数参数不写：可以是任意类型的实参
如下代码：
	1.int fun() 表示返回值为int，接受任意参数的函数，
	2.int fun(void) 表示返回值为int的无参函数。

C++：
	int fun();
	int fun(void); 
	具有相同的意义，都表示返回值为int的无参函数。
--------------------------------------------------
*/


