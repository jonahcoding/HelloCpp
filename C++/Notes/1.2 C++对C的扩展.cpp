/*
--------------------------------------------------
【bool类型】

C++中：
	bool类型只有两个值，true(1值)，false(0值)
	bool类型占1个字节大小
	给bool类型赋值时，非0值会自动转换为true(1)，0值会自动转换false(0)

C语言中：
	c99通过引入stdbool.h头文件，加入bool类型
--------------------------------------------------
*/


