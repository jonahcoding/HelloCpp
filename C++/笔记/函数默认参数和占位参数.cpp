//函数默认参数
int func1(a = 10, b = 10)
{
	return a+b;
}


func();//20
func(20);//30


//如果有一个参数有了默认值，从此参数开始以后的参数必须有默认值。
int func2(a = 10, b = 10, c = 10)
{
	return a+b+c;
}

func2(a,10,20);//40

//函数的声明与实现，只能有一个提供默认参数。
void func3(a=10,b=10);
void func3(a,b){};

//----------------------------------------------------------------

//占位参数：只写一个类型进行占位，调用时必须要传入调用值。（可以有默认值）
int func4(int a, int);
func4(10,1);

int func5(int a, int = 1);
func5(10);

